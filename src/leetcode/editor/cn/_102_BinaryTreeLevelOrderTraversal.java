package leetcode.editor.cn;

import java.util.LinkedList;
import java.util.List;

public class _102_BinaryTreeLevelOrderTraversal {
    public static void main(String[] args) {
        Solution solution = new Solution();
    }

    //leetcode submit region begin(Prohibit modification and deletion)

    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode() {}
     * TreeNode(int val) { this.val = val; }
     * TreeNode(int val, TreeNode left, TreeNode right) {
     * this.val = val;
     * this.left = left;
     * this.right = right;
     * }
     * }
     */
    static class Solution {

        public List<List<Integer>> levelOrder(TreeNode root) {
            final LinkedList<List<Integer>> lists = new LinkedList<>();
            nextLevel(root, lists, 0);
            return lists;
        }

        public void nextLevel(TreeNode root, List<List<Integer>> result, int index) {
            if (root == null) {
                return;
            }
            List<Integer> list;
            if (result.size() <= index) {
                list = new LinkedList<>();
                result.add(list);
            } else {
                list = result.get(index);
            }
            list.add(root.val);
            int nextLevel = index + 1;
            nextLevel(root.left, result, nextLevel);
            nextLevel(root.right, result, nextLevel);
        }
    }

    static class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;

        TreeNode() {
        }

        TreeNode(int val) {
            this.val = val;
        }

        TreeNode(int val, TreeNode left, TreeNode right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }
//leetcode submit region end(Prohibit modification and deletion)

}